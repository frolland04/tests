#include <iostream>
#include <string>

int main() {
    for (const int i : {1, 9, 23, -23, +45})
        std::cout << i << ", " << std::to_string(i) << "\n";

    // 1, 1
    // 9, 9
    // 23, 23
    // -23, -23
    // 45, 45

    for (const double f : {1.0, 9.99, 23.43, 1e-9, 1e40, 1e-40, 123456789.0})
        std::cout << f << ", " << std::to_string(f) << "\n";    
    
    // 1, 1.000000
    // 9.99, 9.990000
    // 23.43, 23.430000
    // 1e-09, 0.000000
    // 1e+40, 10000000000000000303786028427003666890752.000000
    // 1e-40, 0.000000
    // 1.23457e+08, 123456789.000000
}